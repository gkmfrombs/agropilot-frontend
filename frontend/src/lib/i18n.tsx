interface LangCtx {
  lang: Lang
  setLang: (l: Lang) => void
  t: (key: keyof typeof T.en) => string
}

const LangContext = createContext<LangCtx>({
  lang: 'en',
  setLang: () => {},
  t: (k) => k as string,
})

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>(
    () => (localStorage.getItem('agro_lang') as Lang) || 'en'
  )

  const setLang = (l: Lang) => {
    setLangState(l)
    localStorage.setItem('agro_lang', l)
  }

  const t = (key: keyof typeof T.en): string =>
    (T[lang] as any)[key] ?? (T.en as any)[key] ?? key

  return (
    <LangContext.Provider value={{ lang, setLang, t }}>
      {children}
    </LangContext.Provider>
  )
}

export const useTranslation = () => useContext(LangContext)

